const mongoose = require('mongoose');

const PostSchema = mongoose.Schema({

    name: {
        type: String,
        required: true,
    },
    description:{
        type: String,
        required: true,
    },
    createdAt:{
        type: Date,
        default: Date.now,
    },
    image: {
        type: String,
        required: true,
    },
    price: {
        type: Number,
        required: true,
    },
    category: {
        type: String,
        required: true,
    },
});
module.exports = mongoose.model('Posts', PostSchema);
